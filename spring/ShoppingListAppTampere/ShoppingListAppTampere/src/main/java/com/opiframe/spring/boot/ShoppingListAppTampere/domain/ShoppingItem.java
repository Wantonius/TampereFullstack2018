package com.opiframe.spring.boot.ShoppingListAppTampere.domain;

import java.io.Serializable;

/**
 *
 * @author Erno
 */
public class ShoppingItem implements Serializable{
    private long id;
    private String type;
    private int count;
    private float price;
    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the count
     */
    public int getCount() {
        return count;
    }

    /**
     * @param count the count to set
     */
    public void setCount(int count) {
        this.count = count;
    }

    /**
     * @return the price
     */
    public float getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(float price) {
        this.price = price;
    }
    
    @Override
    public String toString() {
        return this.count+", "+this.type+", "+this.price+"â‚¬";
    }
}
